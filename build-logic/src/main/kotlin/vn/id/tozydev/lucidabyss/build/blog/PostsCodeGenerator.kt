import kotlin.time.Instant

private const val PACKAGE_NAME = "vn.id.tozydev.lucidabyss.core"
private const val FILE_NAME = "Posts"
private const val POSTS_PROPERTY_NAME = "Posts"

internal fun generatePostsFileSpec(posts: List<Post>) =
    FileSpec
        .builder(PACKAGE_NAME, FILE_NAME)
        .apply {
            val postType = Post::class.toTypeSpec()
            addType(postType)
            addProperty(
                posts.toPropertySpec(
                    POSTS_PROPERTY_NAME,
                    ClassName(PACKAGE_NAME, requireNotNull(postType.name)),
                ),
            )
        }.build()

/**
 * Converts a map of blog post metadata to a KotlinPoet `PropertySpec` representation.
 */
private fun List<Post>.toPropertySpec(
    name: String,
    postType: TypeName,
) = PropertySpec
    .builder(name, List::class.asClassName().parameterizedBy(postType))
    .initializer(
        CodeBlock
            .builder()
            .apply {
                add("listOf(\n")
                withIndent {
                    this@toPropertySpec.forEach { post ->
                        add(post.toCodeBlockInstance(postType))
                        add(",\n")
                    }
                }
                add(")")
            }.build(),
    ).build()

/**
 * Converts an instance of the `Post` data class into a KotlinPoet `CodeBlock` representation.
 *
 * This function generates a code block that reconstructs a `Post` object using its property values.
 * Example generated code:
 * ```kotlin
 * Post(
 *  slug = "my-post",
 *  route = "my-post",
 *  title = "My Post",
 *  description = "This is my post",
 *  author = "me",
 *  publishedAt = Instant.fromEpochSeconds(1685234567, 0),
 *  modifiedAt = null,
 *  topic = "topic",
 *  tags = setOf("tag1", "tag2"),
 *  coverImage = null,
 * )
 * ```
 */
private fun Post.toCodeBlockInstance(postType: TypeName) =
    CodeBlock
        .builder()
        .apply {
            val kClass = this@toCodeBlockInstance::class
            add("%T(\n", postType)
            withIndent {
                kClass.memberProperties.forEach { prop ->
                    val value = prop.call(this@toCodeBlockInstance)
                    add("${prop.name} = ")

                    when (value) {
                        is String -> {
                            add("%S", value)
                        }

                        is Instant -> {
                            add(
                                "%T.fromEpochSeconds(%L, %L)",
                                Instant::class,
                                value.epochSeconds,
                                value.nanosecondsOfSecond,
                            )
                        }

                        is Set<*> -> {
                            if (value.isEmpty()) {
                                add("emptySet()")
                            } else {
                                add("setOf(${value.joinToString { "\"$it\"" }})")
                            }
                        }

                        null -> {
                            add("null")
                        }

                        else -> {
                            add("%L", value)
                        }
                    }
                    add(",\n")
                }
            }
            add(")")
        }.build()

/**
 * Converts [Post] data class to a KotlinPoet [TypeSpec] representation.
 */
private fun KClass<Post>.toTypeSpec() =
    TypeSpec
        .classBuilder(requireNotNull(simpleName))
        .apply {
            addKdoc(
                """
                Represents a blog post metadata.

                Generated by `build-logic`.
                """.trimIndent(),
            )
            addModifiers(KModifier.DATA)
            primaryConstructor(getPrimaryConstructor())
            memberProperties.forEach {
                addProperty(it.toPropertySpec())
            }
        }.build()

private fun KClass<*>.getPrimaryConstructor() =
    FunSpec
        .constructorBuilder()
        .apply {
            memberProperties.forEach {
                addParameter(it.name, it.returnType.asTypeName())
            }
        }.build()

private fun KProperty1<*, *>.toPropertySpec() =
    PropertySpec
        .builder(name, returnType.asTypeName())
        .initializer(name)
        .build()
